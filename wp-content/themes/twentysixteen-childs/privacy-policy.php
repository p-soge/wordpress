circumstances:</p>
<p>Where you have consented,
Where the secondary purpose is directly related to the primary purpose, and you would reasonably
expect us to use or disclose the information in such a way, or!
Where we are permitted or required by law, or it is in the interests of public safety to do so.!
For example, if you are a client from whom we collect financial information and contact details, we
