functions. You are always provided with the opportunity to notify us that you do not wish to receive
this material.</p>

<div class="green_text">Disclosure of the information we collect</div>
<p>We will not disclose information we collect about you to any third parties without your prior consent.
Similarly, we will not sell any information we collect about you. Depending on the reason for which
personal information was provided, some information collected by one part of the firm may not be
disclosed to other parts of the firm.</p>

<div class="green_text">How we protect our information collection</div>
<p>We will take all reasonable steps to ensure the security of all personal information we collect. This
involves protecting it from misuse and loss, as well as from modification and disclosure. All of our
staff are bound by a confidentiality agreement which is consistent with our privacy statement. The
electronic databases that hold information as well as our internal systems are also designed to
restrict access to only those people who require specific information.</p>

<p>In addition, your personal information is only kept while it is required for the purpose for which it
was collected or for a permitted secondary purpose.</p>

<div class="green_text">Your ability to access and correct information</div>
<p>We will take all reasonable steps to ensure that your personal information which we collect, use or
disclose is accurate, complete and up-to-date. If you wish to access the information we hold about
you, you are welcome to contact us and we will respond to your request within 45 days. A handling
fee may be payable so that we can obtain the information you require.</p>

<div class="green_text">Correcting information</div>
<p>You can also request that information about you be corrected or deleted. This is part of our
commitment to take all reasonable steps to ensure that the information we hold is accurate,
complete and up-to-date.</p>

<div class="green_text">Right to refuse access or correction</div>
<p>We reserve our right to refuse your request for access or correction if, for example, we consider
your request to be frivolous or vexatious, or if we are legally entitled to do so. If we refuse your
request, we will give you our reasons. If we refuse your request to correct information we hold
about you, we will give you the opportunity to post a statement of your claim where we hold your
personal information.</p>

<div class="green_text">Updating our privacy statement</div>
<p>As our Company grows and changes, we may revise our privacy statement. We will keep you
updated on these changes by posting new versions of our policy on our web site. By providing your
personal information to us or by using our web site, you are indicating your acceptance of our
current policy.</p>

<div class="green_text">About Cookies Technology</div>
<p>A cookie file can contain information such as your user ID in order to track the pages you have
visited. A cookie cannot access other information from your hard drive, or from cookies created by
other web sites. Cookies, by themselves, cannot be used to find out the identity of any user. The
only personal information a cookie can contain is information you supply yourself.</p>

